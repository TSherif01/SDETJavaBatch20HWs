package org.example.e60;

public class E60ForLoop {
    public static void main(String[] args) {
        // Use a for loop that starts with i = 10 and continues while i >= 1
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println("Happy New Years!");
        // Inside the loop, print the value of i
        // After the loop, print "Happy New Year!"
    }
}
