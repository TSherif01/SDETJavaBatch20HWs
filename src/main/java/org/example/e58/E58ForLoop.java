package org.example.e58;

public class E58ForLoop {
    public static void main(String[] args) {
        // Use a for loop that starts with i = 1 and continues while i <= 5
        for (int i = 1; i <= 5; i++){
            System.out.println("Processing order #" + i);
        }
        // Inside the loop, print "Processing order #" followed by the value of i
    }
}
