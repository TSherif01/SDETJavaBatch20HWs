package org.example.e59;

public class E59ForLoop {
    public static void main(String[] args) {
        // Use a for loop that starts with year = 2011 and continues while year <= 2019
            for (int year = 2011; year <= 2019; year ++) {
                System.out.println("Hey, it is year:");
                System.out.println(year);
            }
        // Inside the loop, print "Hey, it is year:" and the value of year
    }
}
